package BungeeKSET;

public class spring {
	double k=0; //k constant
	double m=0; //mass
	double length=0; //length of spring
	double displacement=0; //change in length
	
	public spring (double k, double m, double length, double displacement){
		this.k=k;
		this.m=m;
		this.length=length;
		this.displacement=displacement;
	}
}
